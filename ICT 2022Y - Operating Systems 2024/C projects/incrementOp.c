#include <stdio.h>
#include <stdlib.h>

int main(){
    int a,b;
    printf("Enter a number: ");
    scanf("%d",&a);
    printf("Enter another number: ");
    scanf("%d",&b);
    printf("%d\n",a+++b);
    printf("%d\n",a);
    printf("%d\n",b);
    printf("%d\n",++a+b);
    printf("%d\n",a);
    printf("%d\n",b);

    return 0;
}




